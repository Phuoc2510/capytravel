-- Tạo database CapyTravel
CREATE DATABASE CapyTravel;

-- Sử dụng database CapyTravel
USE CapyTravel;

drop database CapyTravel;
drop table IF EXISTS Users; 
drop table IF EXISTS Airport; 
drop table IF EXISTS Airline; 
drop table IF EXISTS Flights; 
drop table IF EXISTS Booking; 
drop table IF EXISTS Luggage;
drop table IF EXISTS Payment; 
drop table IF EXISTS PaymentMethod; 

-- Bảng Users lưu thông tin người dùng
CREATE TABLE Users (
    userID INT PRIMARY KEY,
    firstName NVARCHAR(50),
    lastName NVARCHAR(50),
    userName VARCHAR(50),
    password VARCHAR(255),
    gender VARCHAR(20),
    email VARCHAR(100) UNIQUE,
    phoneNumber VARCHAR(15),
    dateOfBirth DATE,
    identificationNumber VARCHAR(20), -- Bổ sung UNIQUE
	avatar varchar(200),
    role VARCHAR(5),
    status bit
);


-- Bảng Airport lưu thông tin sân bay
CREATE TABLE Airport (
    airportID VARCHAR(30) PRIMARY KEY,
    airportName NVARCHAR(100),
    location NVARCHAR(100),
	status bit
);

-- Bảng Airline lưu thông tin hãng hàng không
CREATE TABLE Airline (
	airlineID VARCHAR(30) PRIMARY KEY,
    airlineName VARCHAR(100),
	airlineStatus bit --xac dinh airline do con kha dung khong
);

-- Bảng Flights lưu thông tin chuyến bay
CREATE TABLE Flights (
    flightID INT PRIMARY KEY,
    flightCode VARCHAR(10) UNIQUE,
    departureDate DATE,
    departureAirport VARCHAR(30), -- FK tới Airport
    ArrivalAirport VARCHAR(30), -- FK tới Airport
    flightTime TIME, -- Thời gian bay
	airlineID VARCHAR(30), -- FK tới Airline
    active BIT DEFAULT 1, -- Trạng thái chuyến bay (1: hoạt động, 0: hủy)
    FOREIGN KEY (departureAirport) REFERENCES Airport(airportID),
    FOREIGN KEY (ArrivalAirport) REFERENCES Airport(airportID),
    FOREIGN KEY (airlineID) REFERENCES Airline(airlineID)
);

--Bảng PaymentMethod lưu các phương thức thanh toán
CREATE TABLE PaymentMethod (
    methodID VARCHAR(20) PRIMARY KEY,
    methodName VARCHAR(50) UNIQUE -- Đặt là UNIQUE để đảm bảo không có giá trị trùng lặp
);

-- Bảng Booking lưu thông tin đặt vé
CREATE TABLE Booking (
    bookingID INT IDENTITY(1,1) PRIMARY KEY, -- Sử dụng IDENTITY để tự động tăng
    userID INT, -- FK tới Users
    departureFlight INT, -- FK tới chuyến bay đi (outbound flight)
    returnFlight INT NULL, -- FK tới chuyến bay về (return flight), NULL nếu là một chiều
    bookingDate DATE,
    totalAmount DECIMAL(10, 2),
    paymentMethod VARCHAR(20),
    FOREIGN KEY (userID) REFERENCES Users(userID),
    FOREIGN KEY (departureFlight) REFERENCES Flights(flightID),
    FOREIGN KEY (returnFlight) REFERENCES Flights(flightID), -- Liên kết tới Flights cho chuyến bay về
	FOREIGN KEY (paymentMethod) REFERENCES PaymentMethod(methodID)
);

-- Bảng Payment lưu thông tin thanh toán
CREATE TABLE Payment (
    paymentID INT IDENTITY(1,1) PRIMARY KEY, -- Sử dụng IDENTITY để tự động tăng
    bookingID INT, -- FK tới Booking
    paymentDate DATE,
    amount DECIMAL(10, 2),
    paymentMethod VARCHAR(20),
    FOREIGN KEY (bookingID) REFERENCES Booking(bookingID),
	FOREIGN KEY (paymentMethod) REFERENCES PaymentMethod(methodID)
);

 
